#include "SDL++/Error.hpp"

////////////////////////////////////////////////////////////////////////////////

namespace SDL::Error
{

////////////////////////////////////////////////////////////////////////////////

std::string get()
{
	return {SDL_GetError()};
}

void clear()
{
	SDL_ClearError();
}

void set(const char *err)
{
	SDL_SetError("%s", err);
}

////////////////////////////////////////////////////////////////////////////////

}